a = 30
b = 70
print(f"a+b={a+b}") #f indica che la stringa che accetta funz, con le graffe si indicano le funzioni
print(f"{a}+{b}={a+b}")
print(f"{a}-{b}={a-b}")
print(f"{a}*{b}={a*b}")
print(f"{a}/{b}={a/b}")
print(f"{a}//{b}={a//b}") #divisone intera
print(f"{a}%{b}={a%b}") #resto o modulo
print(f"{a}**3={a**3}") #potenza

